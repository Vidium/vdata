def test_at_indexer():
    # TDF
    TDF = get_TDF('1')

    # get value
    assert TDF.at[10, 'col1'] == 10

    # set value
    TDF.at[10, 'col1'] = -1
    assert TDF.values_num[60, 0] == -1

    # view
    view = TDF['0h', 50:70, ['col3', 'col1']]

    # get value
    assert view.at[50, 'col3'] == '250'

    # set value
    view.at[50, 'col3'] = '-5'
    assert TDF.values_str[0, 0] == '-5'


